package Class8;

public class NestedLoops2 {
    public static void main(String[] args) {

        for (int i = 0; i < 10; i++) {            //outer loop//

            for (int j = 0; j < 5; j++) {       //inner loop//
                System.out.print("* ");
            }
            System.out.println();
        }

    }}